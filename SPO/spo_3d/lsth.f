      ENTRY POTLS(x,e)
C
C E LONDON
C
      EF1=dEXP(-F*X(1))
      EF2=dEXP(-F*X(2))
      EF3=dEXP(-F*X(3))
      X21=X(1)*X(1)
      X22=X(2)*X(2)
      X23=X(3)*X(3)
      T1=C*(A+X(1)+A1*X21)*EF1
      T2=C*(A+X(2)+A1*X22)*EF2
      T3=C*(A+X(3)+A1*X23)*EF3
      CALL VH2(X,S1,S2,S3)
      XQ1=S1(1)+T1
      XQ2=S2(1)+T2
      XQ3=S3(1)+T3
      XJ1=S1(1)-T1
      XJ2=S2(1)-T2
      XJ3=S3(1)-T3
      XQ=(XQ1+XQ2+XQ3)/2.D0
      XJ=dSQRT(((XJ1-XJ2)**2+(XJ2-XJ3)**2+(XJ3-XJ1)**2)/8.D0)
      ELOND=XQ-XJ
C
C ENS
C
      R=X(1)+X(2)+X(3)
      R2=R*R
      R3=R2*R
      EXNS=dEXP(-FNS*R3)
      ENS = 0.D0
      IF(EXNS.EQ.0.D0) GO TO 20
      WNT=(X(1)-X(2))*(X(2)-X(3))*(X(3)-X(1))
      WN=dABS(WNT)
      WN2=WN*WN
      WN3=WN2*WN
      WN4=WN3*WN
      WN5=WN4*WN
      ENS=(AN1*WN2+AN2*WN3+AN3*WN4+AN4*WN5)*EXNS
C
C NONLINEAR CORRECTIONS
C
   20 COS =(X21+X22+X23)/2.D0
      COS1=(X21-COS)/X(2)/X(3)
      COS2=(X22-COS)/X(1)/X(3)
      COS3=(X23-COS)/X(1)/X(2)
      WB=COS1+COS2+COS3+1.D0
      WB2=WB*WB
      WB3=WB2*WB
      WB4=WB3*WB
      EXF1=dEXP(-F1*R)
      EXF2=dEXP(-F2*R2)
      EXF3=dEXP(-F3*R)
      EB1T=(B1+B2*R)*EXF1
      EB3T=(XL1+XL2*R2)*EXF3
      EB1=WB*(EB1T+EB3T)
      EB2=(WB2*W1+WB3*W2+WB4*W3)*EXF2
      EQ=(X(1)-X(2))**2+(X(2)-X(3))**2+(X(3)-X(1))**2
      RI=1.D0/X(1)+1.D0/X(2)+1.D0/X(3)
      EB4A=WB*D1*EXF1+WB2*D2*EXF2
      EB4B=D3*EXF1+D4*EXF2
      EB4=EB4A*RI+EB4B*WB*EQ
      E=ELOND+ENS+EB1+EB2+EB4 - EZ
c................Energy from au to eV
c      E=E*27.2107
C
	return
C DERIVATIVES
C ***********************************************
C
C E LONDON DERIVATIVES
C
      IF (XJ.EQ.0.0D0) GO TO 1
      XJS=(XJ1+XJ2+XJ3)/8.D0
      T1P=C*(1.D0+2.D0*A1*X(1))*EF1-F*T1
      T2P=C*(1.D0+2.D0*A1*X(2))*EF2-F*T2
      T3P=C*(1.D0+2.D0*A1*X(3))*EF3-F*T3
      ELON1P=(S1(2)+T1P)/2.D0-(S1(2)-T1P)*(.375D0*XJ1-XJS)/XJ
      ELON2P=(S2(2)+T2P)/2.D0-(S2(2)-T2P)*(.375D0*XJ2-XJS)/XJ
      ELON3P=(S3(2)+T3P)/2.D0-(S3(2)-T3P)*(.375D0*XJ3-XJS)/XJ
C
C ENS DERIVATIVES
C
      ENSP1 = 0.D0
      ENSP2 = 0.D0
      ENSP3 = 0.D0
      IF(EXNS.EQ.0.) GO TO 30
      ENSPWN=(AN1*WN*2.D0+AN2*3.D0*WN2+AN3*4.D0*WN3+AN4*5.D0*WN4)*EXNS
      ENSPR=(-3.D0)*FNS*R2*ENS
C
C WN DERIVATIVES
C
      DELTA =-1.D0
      IF (WN.EQ.WNT) DELTA =1.D0
      WNP1=(2.D0*X(1)*(X(3)-X(2))+X22-X23)*DELTA
      WNP2=(2.D0*X(2)*(X(1)-X(3))+X23-X21)*DELTA
      WNP3=(2.D0*X(3)*(X(2)-X(1))+X21-X22)*DELTA
C
C DENS/DXI=(DENS/DWN)(DWN/DXI)+(DENS/DR)(DR/DXI)
C
      ENSP1=ENSPWN*WNP1+ENSPR
      ENSP2=ENSPWN*WNP2+ENSPR
      ENSP3=ENSPWN*WNP3+ENSPR
C
C WB DERIVATIVES
C
   30 WB1P=(X(1)/X(3)-1.D0)/X(2)-1.D0/X(3)-(COS2+COS3)/X(1)
      W23P=(X(3)/X(2)-1.D0)/X(1)-1.D0/X(2)-(COS1+COS2)/X(3)
      WB2P=(X(2)/X(3)-1.D0)/X(1)-1.D0/X(3)-(COS1+COS3)/X(2)
C
      WB3P=(X(3)/X(2)-1.D0)/X(1)-1.D0/X(2)-(COS1+COS2)/X(3)
C
C DEB1/DXI = (DEB1/DWB)(DWB/DXI)+(DEB1/DR)(DR/DXI)
C
      EB1PR=WB*(F1*EB1T+F3*EB3T-B2*EXF1-2.D0*R*XL2*EXF3)
      EB1PWB=EB1T+EB3T
      EB1P1=EB1PWB*WB1P-EB1PR
      EB1P2=EB1PWB*WB2P-EB1PR
      EB1P3=EB1PWB*WB3P-EB1PR
      EB2PWB=(2.D0*WB*W1+3.D0*WB2*W2+4.D0*WB3*W3)*EXF2
      EB2PR=F2*(-2.D0)*R*EB2
      EB2P1=EB2PWB*WB1P+EB2PR
      EB2P2=EB2PWB*WB2P+EB2PR
      EB2P3=EB2PWB*WB3P+EB2PR
C
C DEB4A/DXI= (DEB4A/DWB)(DWB/DXI)+(DEB4A/DRI)(DRI/DXI)+
C             (DEB4A/DR)(DR/DXI)
C
      EB4APW=(D1*EXF1+2.D0*WB*D2*EXF2)*RI
      EB4APR=RI*(WB2*F2*(-2.D0)*R*D2*EXF2-F1*D1*WB*EXF1)
      EB4AP1=EB4APW*WB1P-EB4A/X21+EB4APR
      EB4AP2=EB4APW*WB2P-EB4A/X22+EB4APR
      EB4AP3=EB4APW*WB3P-EB4A/X23+EB4APR
      EB4BPW=EB4B*EQ
      B4BPEQ=EB4B*WB
      EB4BPR=EQ*WB*((-2.D0)*F2*R*D4*EXF2-F1*D3*EXF1)
      EB4BP1=EB4BPW*WB1P+B4BPEQ*(6.D0*X(1)-2.D0*R)+EB4BPR
      EB4BP2=EB4BPW*WB2P+B4BPEQ*(6.D0*X(2)-2.D0*R)+EB4BPR
      EB4BP3=EB4BPW*WB3P+B4BPEQ*(6.D0*X(3)-2.D0*R)+EB4BPR
      E1=(ELON1P+ENSP1+EB1P1+EB2P1+EB4AP1+EB4BP1)*27.2107
      E2=(ELON2P+ENSP2+EB1P2+EB2P2+EB4AP2+EB4BP2)*27.2107
      E3=(ELON3P+ENSP3+EB1P3+EB2P3+EB4AP3+EB4BP3)*27.2107
      RETURN
    1 WRITE (6,2)
    2 FORMAT (1X, 42HEQUILATERAL TRIANGLE, DERIVATIVES INFINITE)
      E1 = 0.0D0
      E2 = 0.0D0
      E3 = 0.0D0
      RETURN
      END
