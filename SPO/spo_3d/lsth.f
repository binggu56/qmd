C    CALL PREPOT ONCE TO INITIALIZE CONSTANTS.
C    THEN CALL POT EVERY TIME YOU WANT POTENTIAL AND ITS DERIVATIVES.
C   THIS IS TRUE LSTH WITH H2 SPLINE FIT CURVES.
C    THIS IS FILE [BCG2.POT]H3LSTH.FOR;1.
